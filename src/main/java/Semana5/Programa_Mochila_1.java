    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        String modelo = cbxMarca.getSelectedItem().toString();
        int cantidadMochilas = Integer.parseInt(txtCantidad.getText());

        String modelos [] = {"Sherman", "Faguo", "Aldo", "Suburban"};
        double precios [] = {149.9, 89.9, 119.9, 174.9};
        int cantidadesDescuento [] = {5, 10, 20};
        double porcentajesDescuento [] = {0.0, 0.05, 0.07, 0.09};
        double importePagarChocolates [] = {0.0, 200.0, 500.0, 700.0};
        int cantidadesChocolates [] = {0, 1, 2, 3};

        int indiceModelo = -1;
        for (int i = 0; i < modelos.length; i++) {
            if (modelos[i].equalsIgnoreCase(modelo)) {
                indiceModelo = i;
                break;
            }
        }

        if (indiceModelo == -1) {
            txtRes.setText("El modelo ingresado no es vÃ¡lido.");
        } else {
            double importeCompra = precios[indiceModelo] * cantidadMochilas;
            double descuento = 0.0;
            for (int i = 0; i < cantidadesDescuento.length; i++) {
                if (cantidadMochilas < cantidadesDescuento[i]) {
                    break;
                }
                descuento = importeCompra * porcentajesDescuento[i];
            }

            double importePagar = importeCompra - descuento;
            int cantidadChocolates = 0;
            for (int i = 0; i < importePagarChocolates.length; i++) {
                if (importePagar < importePagarChocolates[i]) {
                    break;
                }
                cantidadChocolates = cantidadMochilas * cantidadesChocolates[i];
            }

            txtRes.setText("Importe de la compra: S/ " + importeCompra +
                "\nDescuento: S/ " + descuento +
                "\nImporte a pagar: S/ " + importePagar +
                "\nCantidad de chocolates de obsequio: " + cantidadChocolates);
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

