    private void btnProcesoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesoActionPerformed
                                    
        Empleado ep=new Empleado();
        ep.setNombre(txtNombre.getText());
        ep.setHoras(Integer.parseInt(txtHora.getText()));
        ep.setSec(cbxSec.getSelectedIndex());
        txtRes.setText("Nombre  "+ep.getNombre());
        txtRes.append("\n Horas  "+ep.getHoras());
        txtRes.append("\n Pago bruto "+ep.pago());
        txtRes.append("\n afp     "+ep.afp());
        txtRes.append("\n Total "+ep.total());
    }//GEN-LAST:event_btnProcesoActionPerformed

